using System;
using System.Collections.Generic;
using System.Linq;
using System.Text;
using System.Threading.Tasks;

namespace Calculator
{
    public class MathematicalOperations
    {
        public static double Plus(double Q, double W)
        {
            return Q + W;
        }
        public static double Minus(double Q, double W)
        {
            return Q - W;
        }
        public static double Multiplication(double Q, double W)
        {
            return Q * W;
        }
        public static double Divide (double Q, double W)
        {
            return Q / W;
        }
        public static double Modulo (double Q, double W)
        {
            return Q % W;
        }
    }
}
